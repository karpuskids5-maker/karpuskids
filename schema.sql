  return jsonb_build_object(
    'total',            v_students,
    'active',           v_students,
    'teachers',         v_teachers,
    'classrooms',       v_classrooms,
    'attendance_today', v_attendance,
    'pending_payments', v_pending,
    'pending_amount',   v_pending,
    'inquiries',        v_incidents
  );
end;
$$;

